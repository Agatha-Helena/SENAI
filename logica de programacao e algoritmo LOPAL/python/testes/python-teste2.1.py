print("Oi usuário. Vou fazer a média para você de 3 salários!")
num1 = float(input("Digite o primeiro salário: "))
num2 = float(input("Digite o segundo salário: "))
num3 = float(input("Digite o terceiro salário: "))

média=(num1+num2+num3)/3

print("A média do seu salário é de %.2f " %média)
